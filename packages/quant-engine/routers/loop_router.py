class ArchiveRequest(BaseModel):
    link: str
    category: str = "scout_reports"

@router.post("/research/save-to-brain")
async def archive_to_brain(body: ArchiveRequest, request: Request):
    """Save a research finding and its summary as a Markdown file in research/ directory."""
    loop = _get_loop(request)
    
    # Get finding and source info from DB
    cursor = loop.db.execute(
        """
        SELECT f.title, f.description, f.ai_summary, f.timestamp, s.name as source_name 
        FROM research_findings f
        JOIN research_sources s ON f.source_id = s.id
        WHERE f.link = ?
        """, 
        (body.link,)
    )
    row = cursor.fetchone()
    if not row:
        raise HTTPException(status_code=404, detail="Finding not found in DB")
    
    title, description, summary, ts, source_name = row
    
    # Prepare filename: Use topic title (sanitized)
    from datetime import datetime
    safe_title = "".join(x for x in title if x.isalnum() or x in " -_").strip().replace(" ", "_")[:60]
    filename = f"{safe_title}.md"
    
    from pathlib import Path
    if not loop.scout_agent.research_dir:
        raise HTTPException(status_code=500, detail="Research directory not configured")
        
    target_path = Path(loop.scout_agent.research_dir) / filename
    # If file exists, add timestamp to avoid collision but keep title
    if target_path.exists():
        filename = f"{safe_title}_{datetime.now().strftime('%H%M%S')}.md"
        target_path = Path(loop.scout_agent.research_dir) / filename

    target_path.parent.mkdir(parents=True, exist_ok=True)
    
    md_content = f"""# {title}
*Archived from Scout Agent at {datetime.now().isoformat()}*

## Meta
- **Source:** {source_name}
- **Original Link:** {body.link}
- **Analysis Tags:** #scout #alpha #{source_name.replace(' ', '')}

## Project Description
{description}

## AI Analysis & Insights
{summary if summary else "No AI analysis performed yet."}

---
*Generated by Quant Intelligence Hub*
"""
    
    with open(target_path, "w", encoding="utf-8") as f:
        f.write(md_content)
        
    return {"success": True, "filename": filename, "path": str(target_path)}



@router.post("/agents/{agent_name}/trigger")
async def trigger_individual_agent(agent_name: str, body: dict, request: Request):
    loop = _get_loop(request)
    if agent_name == "scout_agent":
        topic = await loop.scout_agent.scout_for_alpha()
        loop._add_log(f"Manual: ScoutAgent found topic: {topic}")
        return {"result": topic}
    elif agent_name == "scout_agent_scrape":
        source_id = body.get("source_id")
        findings = await loop.scout_agent.run_scrape_only(source_id=source_id)
        loop._add_log(f"Manual: ScoutAgent synced {len(findings)} ideas to DB from source {source_id or 'all'}")
        return {"findings": findings}
    elif agent_name == "alpha_agent":
        topic = body.get("topic", "auto")
        res = await loop.alpha_agent.generate_strategy(topic)
        return res
    elif agent_name == "sentiment_agent":
        await loop.sentiment_agent.compute_all()
        return {"status": "success"}
    elif agent_name == "data_agent":
        await loop.data_agent.run_etl_all()
        return {"status": "success"}
    elif agent_name == "decay_check":
        decayed = await loop.run_decay_check()
        return {"decayed_strategies": decayed}
    else:
        raise HTTPException(status_code=400, detail=f"Agent '{agent_name}' not triggerable")


@router.get("/history")
async def get_loop_history(request: Request) -> list[dict]:
    loop = _get_loop(request)
    cursor = loop.db.execute("SELECT cycle_id, started_at, completed_at, strategies_generated, strategies_approved, strategies_rejected, errors FROM cycle_history ORDER BY started_at DESC LIMIT 100")
    rows = cursor.fetchall()
    import json
    return [{"cycle_id": r[0], "started_at": r[1], "completed_at": r[2], "strategies_generated": r[3], "strategies_approved": r[4], "strategies_rejected": r[5], "errors": json.loads(r[6]) if r[6] else []} for r in rows]

# ─── Research Sources Endpoints ──────────────────────────────────────────────

class SourceCreateRequest(BaseModel):
    name: str
    type: str
    url: str
    config: dict = {}

class SourcePatchRequest(BaseModel):
    enabled: bool | None = None
    url: str | None = None
    config: dict | None = None

@router.get("/research-sources")
async def get_research_sources(request: Request):
    loop = _get_loop(request)
    return loop.scout_agent.get_sources()

@router.post("/research-sources")
async def add_research_source(body: SourceCreateRequest, request: Request):
    loop = _get_loop(request)
    source_id = loop.scout_agent.add_source(body.name, body.type, body.url, body.config)
    return {"id": source_id, "success": True}

@router.patch("/research-sources/{source_id}")
async def update_research_source(source_id: int, body: SourcePatchRequest, request: Request):
    loop = _get_loop(request)
    loop.scout_agent.update_source(source_id, enabled=body.enabled, url=body.url, config=body.config)
    return {"success": True}

@router.delete("/research-sources/{source_id}")
async def delete_research_source(source_id: int, request: Request):
    loop = _get_loop(request)
    loop.scout_agent.delete_source(source_id)
    return {"success": True}
